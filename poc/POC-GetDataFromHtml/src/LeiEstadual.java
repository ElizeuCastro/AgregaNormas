public class LeiEstadual {

	String ano;
	String numero;
	String data;
	String ementa;
	String linkLei;
	String linkLeiDetalhe;

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getEmenta() {
		return ementa;
	}

	public void setEmenta(String ementa) {
		this.ementa = ementa;
	}

	public String getLinkLei() {
		return linkLei;
	}

	public void setLinkLei(String linkLei) {
		this.linkLei = linkLei;
	}

	public String getLinkLeiDetalhe() {
		return linkLeiDetalhe;
	}

	public void setLinkLeiDetalhe(String linkLeiDetalhe) {
		this.linkLeiDetalhe = linkLeiDetalhe;
	}

}
